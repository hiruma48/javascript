function testingswitch(){
    var nilai = 8;

    switch(nilai){
        case 10:
            console.log("sangat baik");
            break;
        case 8:
            console.log("baik");
            break;
        case 6:
            console.log("cukup");
            break;
        default:
            console.log("lainnya")
    }
}
testingswitch()